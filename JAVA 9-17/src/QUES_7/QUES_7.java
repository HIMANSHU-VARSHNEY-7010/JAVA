package QUES_7;

import java.util.List;
import java.util.Map;
import java.util.Set;

//      7. Use Of method to create List, Set and Map
public class QUES_7 {
    public static void main(String[] args) {
        System.out.println(List.of(1,2,3,4,5,3,7,8));
        System.out.println(Set.of(1,2,3,4,5,7));
        System.out.println(Map.of(1,"HIMANSHU", 2,"SOURABH", 3, "INDER"));
    }
}
